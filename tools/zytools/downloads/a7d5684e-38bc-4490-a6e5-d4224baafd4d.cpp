#include <iostream>
using namespace std;

int main() {
   double caffeineMg;  // "double" supports floating-point like 75.5, versus int for integers like 75.
   
   cin >> caffeineMg;

   caffeinMg = caffeineMg / 2;
   cout<<"After 6 hours: "<<caffeinMg<<endl;
   caffeinMg = caffeineMG / 2;
   cout<<"After 12 hours: "<<caffeinMg<<endl;
   caffeinMg = caffeineMG / 2;
   cout<<"After 18 hours: "<<caffeinMg<<endl;

   return 0;
}
